    private Student getStudentByID (String id) {
        String sql = "SELECT * FROM Student WHERE Sid = '" + id + "'";
        try {
            PreparedStatement ps = connection.prepareCall(sql);
            ResultSet rs = ps.executeQuery();
            rs.next();
            String sid = rs.getString("Sid");
            String name = rs.getString("Name");
            int age = rs.getInt("Age");
            String major = rs.getString("Major");
            float gpa = rs.getFloat("GPA");
            int hrs = rs.getInt("Hrs_completed");
            Student t = new Student(id, name, age, major, gpa, hrs);
            return t;
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return null;
    }
    
    private Course getCourse (String id, String term) {
        String sql = "SELECT * FROM Course WHERE Cid = '" + id + "' AND Term = '" + term + "'";
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            rs.next();
            String cid = rs.getString("Cid");
            String Term = rs.getString("Term");
            String name = rs.getString("Name");
            String dept = rs.getString("Dept");
            int enrollment = rs.getInt("Enrollment");
            Course c = new Course(cid, Term, name, dept, enrollment);
            return c;
        } catch (SQLException ex) {
            ex.printStackTrace();
                    
        }
        return null;
    }
    
    private void ShowTable() {
        tm.setRowCount(0);
        String sql = "SELECT * FROM Take ORDER BY length(Sid),Sid";
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String sid = rs.getString("Sid");
                String cid = rs.getString("Cid");
                String term = rs.getString("Term");
                Student student = getStudentByID(sid);
                Course course = getCourse(cid, term);
                String grade = rs.getString("Grade");
                Take t = new Take(student, course, grade);
                tm.addRow(t.toObject());
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } 
    }
