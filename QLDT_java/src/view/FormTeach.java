    private Professor getProfessorByID (String id) {
        String sql = "SELECT * FROM Professor WHERE Pid = '" + id + "'";
        try {
            PreparedStatement ps = connection.prepareCall(sql);
            ResultSet rs = ps.executeQuery();
            rs.next();
            String pid = rs.getString("Pid");
            String name = rs.getString("Name");
            String dept = rs.getString("Dept");
            int yrs = rs.getInt("Yrs_teaching");
            Professor t = new Professor(id, name, dept, yrs);
            return t;
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return null;
    }
    
    private Course getCourse (String id, String term) {
        String sql = "SELECT * FROM Course WHERE Cid = '" + id + "' AND Term = '" + term + "'";
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            rs.next();
            String cid = rs.getString("Cid");
            String Term = rs.getString("Term");
            String name = rs.getString("Name");
            String dept = rs.getString("Dept");
            int enrollment = rs.getInt("Enrollment");
            Course c = new Course(cid, Term, name, dept, enrollment);
            return c;
        } catch (SQLException ex) {
            ex.printStackTrace();
                    
        }
        return null;
    }
    
    private void ShowTable() {
        tm.setRowCount(0);
        String sql = "SELECT * FROM Teach ORDER BY length(Pid),Pid";
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String pid = rs.getString("Pid");
                String cid = rs.getString("Cid");
                String term = rs.getString("Term");
                Professor professor = getProfessorByID(pid);
                Course course = getCourse(cid, term);
                Teach t = new Teach(professor, course);
                tm.addRow(t.toObjects());
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } 
    }
