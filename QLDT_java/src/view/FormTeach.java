    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i >= 0 && i<tm.getRowCount()) {
            String pid = tm.getValueAt(i, 0).toString();
            String cid = tm.getValueAt(i, 1).toString();
            String term = tm.getValueAt(i, 2).toString();
            String sql = "DELETE FROM Teach WHERE Pid = '" + pid + "' AND Cid = '" + cid 
                    + "' AND Term = '" + term + "'";
            try {    
                PreparedStatement ps = connection.prepareStatement(sql);
                int r = ps.executeUpdate();
                if (r>0) {
                    tm.removeRow(i);
                    return;
                }
            }catch (SQLException ex) {
                ex.printStackTrace();
            }   
        }
        JOptionPane.showMessageDialog(this, "Choose a row");
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        try {
            // TODO add your handling code here:
            String pid = cbb1.getSelectedItem().toString();
            String cid = cbb2.getSelectedItem().toString();
            String term = cbb3.getSelectedItem().toString();
            Professor professor = getProfessorByID(pid);
            Course course = getCourse(cid, term);
            Teach t = new Teach(professor, course);
            String sql = "INSERT INTO Teach VALUES(?,?,?)";
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, pid);
            ps.setString(2, cid);
            ps.setString(3, term);
            int r = ps.executeUpdate();
            if(r > 0) {
                tm.addRow(t.toObjects());
                return;
            }
            else {
                JOptionPane.showMessageDialog(this, "Error!");
            }
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btAddActionPerformed

